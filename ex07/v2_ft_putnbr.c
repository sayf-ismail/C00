#include <unistd.h>

void	ft_putnbr(int nb)
{
	char c;
	
	if (nb < 0)
	{
		c = '-';
		write(1, &c, 1);

		c = '0' - nb;
		write(1, &c, 1);
	}
	
	if (nb > 0)
	{
		c = '0' + nb;
		write(1, &c, 1);
	}
	c = '\n';
	write(1, &c, 1);
}
